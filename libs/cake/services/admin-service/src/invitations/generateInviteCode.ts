export function generateInviteCode(prefix?: string) {
  const color =
    colors[Math.floor(Math.random() * colors.length)].toLocaleLowerCase();
  const noun =
    nouns[Math.floor(Math.random() * nouns.length)].toLocaleLowerCase();

  return `${color}-${noun}-${generateRandomString(4)}`;
}

function generateRandomString(length: number) {
  const characters =
    "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
  let result = ""; // Initialize the result variable to store the random string

  for (let i = 0; i < length; i++) {
    const randomIndex = Math.floor(Math.random() * characters.length);
    result += characters.charAt(randomIndex);
  }

  return result;
}

